// Fonction utilitaire pour obtenir une traduction
export const t = (key, lang = null) => {
  // Si aucune langue n'est fournie, essayer de la récupérer depuis localStorage
  if (!lang) {
    const savedLanguage = localStorage.getItem('interfaceLanguage');
    const savedSettings = localStorage.getItem('appearanceSettings');
    
    if (savedSettings) {
      try {
        const parsed = JSON.parse(savedSettings);
        lang = parsed.interfaceLanguage || savedLanguage || 'Français';
      } catch (e) {
        lang = savedLanguage || 'Français';
      }
    } else {
      lang = savedLanguage || 'Français';
    }
  }
  
  return translations[lang]?.[key] || key;
};

